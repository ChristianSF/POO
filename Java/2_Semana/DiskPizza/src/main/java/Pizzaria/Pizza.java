package Pizzaria;

public class Pizza {
    
    private int code;
    private String nome;
    private double preco;
    private String Ingredientes[];

    public int getCode() {
        return code;
    }

    public String getNome() {
        return nome;
    }

    public double getPreco() {
        return preco;
    }

    public String[] getIngredientes() {
        return Ingredientes;
    }
}
